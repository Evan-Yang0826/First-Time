/* 本地头文件 */
#define STM32_DELAY_LOCAL
#include "STM32_delay.h"


/*************************************************************************************************
** 函数名称:    delay_us
** 功能描述:    延时指定微秒数
** 输　  入:    nTimer 延时时间 单位 微秒
** 输　  出:    无
** 返 回 值:    无
** 先决条件:    无
** 全局变量:      
** 调用模块:      
**--------------------------------------------------------------------------------------------
** 修改记录:      
** 2018/10/12   Joshua    创建
*************************************************************************************************/
void delay_us(uint32_t nTimer)
{		
	INT32U i;
	for(i=0;i<nTimer;i++)
	{
		__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();
		__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();
		__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();
		__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();
		__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();__NOP();
	}
			    
}

/*************************************************************************************************
** 函数名称:    delay_ms
** 功能描述:    延时指定毫秒数
** 输　  入:    nTimer 延时时间 单位 毫秒
** 输　  出:    无
** 返 回 值:    无
** 先决条件:    无
** 全局变量:      
** 调用模块:      
**--------------------------------------------------------------------------------------------
** 修改记录:      
** 2018/10/12   Joshua    创建
*************************************************************************************************/
void delay_ms(INT32U nTimer)
{
	INT32U i=1000*nTimer;
	delay_us(i);
}


